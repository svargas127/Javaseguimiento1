package Operadores2;
    public class IncrementoDecremento {
        public static void main(String[] args) {
            int x = 5;
            int y = 3;
            System.out.println("*** Operador de incremento ***");
            x++; // Incrementa x en 1
            System.out.println("x después del incremento: " + x);

            System.out.println("*** Operador de decremento ***");
            y--; // Reduce y en 1
            System.out.println("y después del decremento: " + y);

        }
    }